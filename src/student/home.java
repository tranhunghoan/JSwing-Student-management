    private boolean isNumeric(String s) {
        try {
            double d = Double.parseDouble(s);
            if (d >= 0.0 && d <= 4.0) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a valid value, it must be between 0.0 to 4.0");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "" + e);
        }
        return false;
    }

    public boolean check() {
        String newEmail = jTextField3.getText();
        String newPhone = jTextField4.getText();
        String oldEmail = model.getValueAt(rowIndex, 4).toString();
        String oldPhone = model.getValueAt(rowIndex, 5).toString();

        if (newEmail.equals(oldEmail) && newPhone.equals(oldPhone)) {
            return false;
        }

        if (!newEmail.equals(oldEmail)) {
            boolean emailExists = student.isDuplicateEmail(newEmail);
            if (emailExists) {
                JOptionPane.showMessageDialog(this, "Email already exists");
                return true;
            }
        }

        if (!newPhone.equals(oldPhone)) {
            boolean phoneExists = student.isDuplicatePhone(newPhone);
            if (phoneExists) {
                JOptionPane.showMessageDialog(this, "Phone number already exists");
                return true;
            }
        }

        return false;
    }

    private ImageIcon imageAdjust(String imagePath, byte[] pic) {
        ImageIcon myImage = null;

        if (imagePath != null) {
            byte[] imageBytes = pic; // Assuming 'pic' is the byte array representing the image

            if (imageBytes != null) {
                myImage = new ImageIcon(imageBytes);
            } else {
                myImage = new ImageIcon(imagePath);
            }

            Image img = myImage.getImage();
            img = img.getScaledInstance(jLabelImage.getWidth(), jLabelImage.getHeight(), Image.SCALE_DEFAULT);

            ImageIcon icon = new ImageIcon(img);
            return icon;
        }

        return null;
    }

